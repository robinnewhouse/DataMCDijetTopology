def toEVNT(exot3_name):
  evnt_str = exot3_name.replace("merge.DAOD_EXOT3", "evgen.EVNT")
  substrs = evnt_str.split(".")
  substrs[-1] = substrs[-1][0:5]
  return '.'.join(substrs)

def toAOD(exot3_name):
  aod_str = exot3_name.replace("merge.DAOD_EXOT3", "merge.AOD")
  substrs = aod_str.split(".")
  substrs[-1] = substrs[-1][:-6]
  return '.'.join(substrs)

